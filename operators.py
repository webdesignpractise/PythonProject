print("Product of "+str(a)+" and "+str(b)+" is "+ str(a*b))
print("Divison of "+str(a)+" and "+str(b)+" is "+ str(a/b))
print("Floor Division of "+str(a)+" and "+str(b)+" is "+ str(a//b))
print("Remainder of "+str(a)+" and "+str(b)+" is "+ str(a%b))
